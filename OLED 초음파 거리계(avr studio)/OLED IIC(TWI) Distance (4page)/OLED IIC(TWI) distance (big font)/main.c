//SSD1306 커맨드 전송 함수
void write(uint8_t com)
{
	TWCR=0xA4;	//Send START
	while (!(TWCR & (1<<TWINT)));

	TWDR=add;	//slave address + R/W
	TWCR=0x84;
	while (!(TWCR & (1<<TWINT)));
	
	TWDR=0x00;	//command 모드
	TWCR=0x84;
	while (!(TWCR & (1<<TWINT)));

	TWDR=com;	//data 출력
	TWCR=0x84;
	while (!(TWCR & (1<<TWINT)));

	TWCR=0x94;	//Send STOP
}

//SSD1306 데이터 전송 함수
void data_write(uint8_t data)
{
	TWCR=0xA4;	//Send START
	while (!(TWCR & (1<<TWINT)));

	TWDR=add;	//slave address + R/W
	TWCR=0x84;
	while (!(TWCR & (1<<TWINT)));
	
	TWDR=0x40;	//data 모드
	TWCR=0x84;
	while (!(TWCR & (1<<TWINT)));

	TWDR=data;	//data 출력
	TWCR=0x84;
	while (!(TWCR & (1<<TWINT)));

	TWCR=0x94;	//Send STOP
}


//128x32 비트맵 전송 함수
void draw(const unsigned char *ch)
{
	unsigned char x,y;
	unsigned int i=0;

	for(y=0;y<4;y++)
	{
		write(0xb0+y);	//(Y축 주소 지정 커맨드 범위(B0~B3))
		write(0x00);	// X축 주소
		write(0x10);	// X축 주소

		for(x=0;x<128;x++)
		{
			data_write(pgm_read_byte(&ch[i++]));		//ATMEGA8의 EEPROM이 아닌 플래시 메모리에 배열을 저장했기 때문에 pgm_read_byte로 읽어야한다.
		}
	}
}

